<?php

// ==, !=, === !==

  $a = 5;
  $b = 3;

  if($a == $b) { // Igualdade
    echo $a . " não é igual a <br>" . $b;

}
  if($a != $b) { // Diferença
    echo $a . " é diferente de " . $b; echo "<br>";
  }

  if($a === $b) { // Identica
    echo $a . " é identico a " . $b; echo "<br>";
  }

  if($a !== $b) { // Não Identica
    echo $a . " Não é identico a " . $b; echo "<br>";
  }

